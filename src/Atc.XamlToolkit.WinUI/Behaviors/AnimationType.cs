namespace Atc.XamlToolkit.Behaviors;

/// <summary>
/// Defines the types of animations available.
/// </summary>
public enum AnimationType
{
    /// <summary>
    /// Fade in animation.
    /// </summary>
    FadeIn,

    /// <summary>
    /// Fade out animation.
    /// </summary>
    FadeOut,

    /// <summary>
    /// Slide in from left animation.
    /// </summary>
    SlideInFromLeft,

    /// <summary>
    /// Slide in from right animation.
    /// </summary>
    SlideInFromRight,

    /// <summary>
    /// Slide in from top animation.
    /// </summary>
    SlideInFromTop,

    /// <summary>
    /// Slide in from bottom animation.
    /// </summary>
    SlideInFromBottom,

    /// <summary>
    /// Scale in animation.
    /// </summary>
    ScaleIn,

    /// <summary>
    /// Scale out animation.
    /// </summary>
    ScaleOut,
}